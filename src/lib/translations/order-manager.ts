export function getOrderTranslation(lang: "en" | "ru", key: string): string {
  const keys = key.split(".");
  let value: unknown = orderManagerTranslations[lang];
  
  for (const k of keys) {
    if (value && typeof value === "object" && k in value) {
      value = (value as Record<string, unknown>)[k];
    } else {
      return key;
    }
  }
  
  return typeof value === "string" ? value : key;
}
